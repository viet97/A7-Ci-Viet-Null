public class KeySetting {
    public int keyup;
    public int keydown;
    public int keyleft;
    public int keyright;

    public KeySetting(int keyup, int keydown, int keyleft, int keyright) {
        this.keyup = keyup;
        this.keydown = keydown;
        this.keyleft = keyleft;
        this.keyright = keyright;
    }
}
